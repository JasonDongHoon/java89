/* 주제: 자바 기본 도구 사용법 - 입력 도구 Scanner
=> 입력 받은 데이터를 특정 타입의 데이터로 잘라주는 도구
*/

package step09;

import java.util.Scanner;

public class Exam062_00 {
  public static void main(String[] args) {
    boolean first = true;
    while (true) {
      int i;
      if (first) {
        i = 0;
        first = false;
      } else {
        i++;
      }
      System.out.println(i);
    }
  }




}

